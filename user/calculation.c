{
	*p  =u8_to_char(buf / 1000000000);
	*++p=u8_to_char(buf % 1000000000 / 100000000);
	*++p=u8_to_char(buf % 100000000 /  10000000);
	*++p=u8_to_char(buf % 10000000 /   1000000);
	*++p=u8_to_char(buf % 1000000 /    100000);
	*++p=u8_to_char(buf % 100000 /	   10000);
	*++p=u8_to_char(buf % 10000 /      1000);
	*++p=u8_to_char(buf % 1000 /       100);
	*++p=u8_to_char(buf % 100 /        10);
	*++p=u8_to_char(buf % 10);
}

/*
	函数名： u8 u8_to_char(u8 buf)
	描述：	 将0~15的十进制数转换为0~9~A~F的字符
	输入：	 0~15的十进制数,若输入大于15，则返回0x01;
	返回：	 0~9~A~F的字符，A~F为大写
*/
u8 u8_to_char(u8 buf)
{
	if(buf < 10)
	{
		return buf+0x30;
	}
	else if(buf < 16)
	{
		return buf+0x41-10;
	}
	else 
		return 0x01;
}

/*
*	函数名：Check_Data(u8 *pointer, u8 len)
*	描述：	从起始字开始按字节累加到校验字前，取低位结果，然后取反，
			传输时将取反的结果转换成2字节ASCII数值，并高位在前。例
			如取反后的结果是0x4B，转换成2字节ASCII数值‘4’‘B’，传输
			时‘4’在前。
*	输入：	*pointer数据指针入口，u8 len 数据长度
*	输出：	无
*	返回：	若为0则校验成功
*/
u8 Check_Data(u8 *pointer, u8 len)
{
	u8 i,a,b,c;
	for(i=0;i<len-4;i++) a+=*pointer++;
	a=a+1;
	a=~a;;
	b=a/16;
	if(b>=10) b=b+55;
	else b=b+48;
	c=a%16;
	if(c>=10) c=c+55;
	else c=c+48;
	if((b==*(pointer)) && (c==*(pointer+1))) return 1;
	else 
	return 0;
}

