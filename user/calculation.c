/*
*	函数名：U32_To_Char(u32 a)
*	描述：	将一个无符号长整型数据(unsigned long int)转换为字符串显示
*	输入：	u32 a,一个长整型数据
*	输出： 	无
*	返回：	无
*/
void U32_To_Char(u32 a)
{
	u8 c[10],i;
	c[0]=a/1000000000+48;
	c[1]=(a%1000000000)/100000000+48;
	a=(a%1000000000)%100000000;
	c[2]=a/10000000+48;
	c[3]=(a%10000000)/1000000+48;
	a=(a%10000000)%1000000;
	c[4]=a/100000+48;
	c[5]=(a%100000)/10000+48;
	a=(a%100000)%10000;
	c[6]=a/1000+48;
	c[7]=(a%1000)/100+48;
	a=(a%1000)%100;
	c[8]=a/10+48;
	c[9]=a%10+48;
	for(i=0;i<=9;i++)
	USART1_TxBuffer[i]=c[i];
	USART1_TxBuffer[10]=0x0D;
	USART1_TxBuffer[11]=0x0A;
	USART1_TxLen=12;
	//USART1_SendDW();
}

/*
	函数名：Data_CRC(u8 *p1,u8 p2,u8 len)
	描述：	计算一组数组的CRC值，从指针p1处开始计算，长度为len，所有数据相加，
			取反，然后以ASCII码发送出去。
	输入：	*p1被计算数组指针，*p2输出数组指针，len被计算数组长度
	输出：	无
	返回：	无
*/	
//unsigned short  CRC16(unsigned char *puchMsg,unsigned short usDataLen)
void Data_CRC(unsigned char *puchMsg, u8 *p2, unsigned short usDataLen)
{
	u16 sum;
	u8 i;
	unsigned char uchCRCHi = 0xFF;		//初始化高字节
	unsigned char uchCRCLo = 0xFF;		//初始化低字节
	unsigned short uIndex;		//把CRC表
	while(usDataLen--)
	{
		uIndex=uchCRCHi ^ *puchMsg++;	//计算CRC
		uchCRCHi=uchCRCLo ^ auchCRCHi[uIndex];
		uchCRCLo=auchCRCLo[uIndex];
	}
	sum=uchCRCHi<<8 | uchCRCLo;
	i=sum/0x1000;
	if(i>=10) i=i+55;
	else i=i+48;
	*p2=i;
	i=(sum % 0x1000) /0x100;
	if(i>=10) i=i+55;
	else i=i+48;
	*++p2=i;
	sum=sum % 0x1000 %0x100;
	i=sum/0x10;
	if(i>=10) i=i+55;
	else i=i+48;
	*++p2=i;
	i=sum%0x10;
	if(i>=10) i=i+55;
	else i=i+48;
	*++p2=i;
}
//标准CRC算法
unsigned short CRC16(unsigned char *puchMsg,unsigned short usDataLen)
{
	unsigned char uchCRCHi = 0xFF;		//初始化高字节
	unsigned char uchCRCLo = 0xFF;		//初始化低字节
	unsigned short uIndex;		//把CRC表
	while(usDataLen--)
	{
		uIndex=uchCRCHi ^ *puchMsg++;	//计算CRC
		uchCRCHi=uchCRCLo ^ auchCRCHi[uIndex];
		uchCRCLo=auchCRCLo[uIndex];
	}
	return (uchCRCHi<<8 |uchCRCLo);
}

